def scramble(text):
    words = text.split()
    for i in range(len(words)):
        w = words[i]
        if len(w) > 3:
            mid = list(w[1:-1])
            shuffle(mid)
            words[i] = w[0] + ''.join(mid) + w[-1]
    return ' '.join(words)


def rev(text):
    d = {
        '(': ')',
        ')': '(',
        '[': ']',
        ']': '[',
        '{': '}',
        '}': '{',
        '?': 'Â¿',
        'Â¿': '?',
        '!': 'Â¡',
        'Â¡': '!',
        '/':'\\',
        '\\':'/',
        'Â´': '`',
        '`': 'Â´',
    }
    return ''.join(d.get(c, c) for c in reversed(text))


def circle(text):
    def work(text):
        for c in text:
            o = ord(c)
            if 97 <= o <= 122:
                yield chr(o + 9327)
            elif 65 <= o <= 90:
                yield chr(o + 9333)
            elif 49 <= o <= 57:
                yield chr(o + 9263)
            elif o == 48:
                return chr(9450)
            else:
                yield c

    return ''.join(work(text))


